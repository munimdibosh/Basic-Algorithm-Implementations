package com.dibosh.experiments.datastructures;

public abstract class DataContainer {
	
	/**
	 * Returns the size of the stack
	 * @return
	 */
	abstract public int size();
	/** 
	 * Returns true if the stack is empty
	 * @return
	 */
	abstract public boolean isEmpty();
	
}
